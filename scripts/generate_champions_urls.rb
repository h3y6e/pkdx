def normalize_form(name)
  name.gsub(/\s+/, '').gsub('（', '(').gsub('）', ')')
end

matched = {}
unmatched = []

ALL_POKEMON.each do |pokemon|
  # 手動URLチェック
  if MANUAL_URLS[pokemon]
    matched[pokemon] = MANUAL_URLS[pokemon]
    next
  end

  # エイリアス変換
  lookup_key = FORM_ALIASES[pokemon] || pokemon
  norm = normalize_form(lookup_key)

  url = YAKKUN_MAP[lookup_key]

  unless url
    YAKKUN_MAP.each do |key, val|
      if normalize_form(key) == norm
        url = val
        break
      end
    end
  end

  if url
    # 全パスを /ch/ に変換（/za/, /sv/, /swsh/ 等）
    ch_url = url.gsub(%r{/(?:za|sv|swsh|sm|bdsp|legends_arceus)/zukan/}, '/ch/zukan/')
    matched[pokemon] = ch_url
  else
    unmatched << pokemon
  end
end

result = {
  matched_count: matched.size,
  unmatched_count: unmatched.size,
  unmatched: unmatched,
  urls: matched,
}

puts JSON.pretty_generate(result)
